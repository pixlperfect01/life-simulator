function player(id){
  this.name="Player "+(id+1);
  this.color="#FF0000";
  this.x=0;
  this.y=0;
  this.money=0;
  this.lives=5;
  this.health=3;
  this.id=id;
  this.move=false;
  if(id===0){
    this.up="keys.up";
    this.down="keys.down";
    this.left="keys.left";
    this.right="keys.right";
  }else if(id===1){
    this.up="keys.w";
    this.down="keys.s";
    this.left="keys.a";
    this.right="keys.d"
  }else if(id===2){
    this.up="keys.t";
    this.down="keys.g";
    this.left="keys.f";
    this.right="keys.h";
  }else if(id===3){
    this.up="keys.i";
    this.down="keys.k";
    this.left="keys.j";
    this.right="keys.l";
  }
  this.powerUps=[0,0,0,0,0,0,0];//high jump,dbl jump,flight,ex health,sheild,dbl coins,speed boost
  this.uC=0;
  this.draw=function(){
    ctx.fillStyle=this.color;
    ctx.fillRect(this.x,this.y,20,20);
    ctx.beginPath();
    ctx.arc(this.x+10,this.y+10,7,0,360);
    ctx.fillStyle="#FFFFFF";
    ctx.strokeStyle="#000000";
    ctx.lineWidth=0;
    ctx.fill();
    ctx.stroke();
    ctx.beginPath();
    if(this.move!==false){
      if(this.move==="left")
        ctx.arc(this.x+7,this.y+10,3,0,2*Math.PI);
      if(this.move==="right")
        ctx.arc(this.x+13,this.y+10,3,0,2*Math.PI);
      if(this.move==="up")
        ctx.arc(this.x+10,this.y+7,3,0,2*Math.PI);
      if(this.move==="down")
        ctx.arc(this.x+10,this.y+13,3,0,2*Math.PI);
    }else{
      ctx.arc(this.x+10,this.y+10,3,0,2*Math.PI);
    }
    ctx.fillStyle="#000000";
    ctx.fill();
    ctx.stroke();
  }
  this.left=function(){
    this.move="left";
    this.x--;
  }
  this.right=function(){
    this.move="right";
    this.x++;
  }
  this.up=function(){
    this.move="up";
    this.y--;
  }
  this.down=function(){
    this.move="down";
    this.y++;
  }
  this.stay=function(){
    this.move=false;
  }
}
